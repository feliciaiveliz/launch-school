# There are a number of variables listed below. What are the different types and how do you know which is which?

excited_dog = "excited dog"
@excited_dog = "excited dog"
@@excited_dog = "excited dog"

# 1. Local variable. They do not start with anything but a lowercase letter
# 2. Instance variable. These start with one '@' in front of the lowercase name
# 3. Class variable. These start with two '@@'.